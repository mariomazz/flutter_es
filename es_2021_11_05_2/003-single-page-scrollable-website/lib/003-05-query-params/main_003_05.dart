import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';
import 'package:single_page_scrollable_website/003-05-query-params/router/single_page_app_route_information_parser_05.dart';
import 'package:single_page_scrollable_website/003-05-query-params/router/single_page_app_router_delegate_05.dart';

import '../configure_nonweb.dart' if (dart.library.html) '../configure_web.dart';

void main() {
  configureApp();
  runApp(MyApp());
}

class MyApp extends StatefulWidget {
  @override
  _MyAppState createState() => _MyAppState();
}

class _MyAppState extends State<MyApp> {
  late SinglePageAppRouterDelegate delegate;
  late SinglePageAppRouteInformationParser parser;
  final _colors = Colors.primaries.reversed.toList();

  @override
  void initState() {
    super.initState();
    delegate = SinglePageAppRouterDelegate(colors: _colors);
    parser = SinglePageAppRouteInformationParser(colors: _colors);
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp.router(
      debugShowCheckedModeBanner: false,
      routerDelegate: delegate,
      routeInformationParser: parser,
    );
  }
}
